export function downloadJson(payload, filename, { space } = {}) {
  const blob = new Blob([JSON.stringify(payload, null, space)], { type: "application/json" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
